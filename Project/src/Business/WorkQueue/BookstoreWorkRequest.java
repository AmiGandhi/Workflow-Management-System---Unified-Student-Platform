package Business.WorkQueue;

/**
 *
 * @author Ami Gandhi
 */
public abstract class BookstoreWorkRequest extends WorkRequest {
    
     //data for Bookstore Enterprise
    String bookName;
    double bookPrice;
    String bookAuthor;
    String booktype;
    int noOfBooks;
    String fName;
    String lName;
    String buyerName;
    String trackCartUser;
    
    // getter and setter for Bookstore

    public String getBookName() {
        return bookName;
    }

    public void setBookName(String bookName) {
        this.bookName = bookName;
    }

    public double getBookPrice() {
        return bookPrice;
    }

    public void setBookPrice(double bookPrice) {
        this.bookPrice = bookPrice;
    }

    public String getBookAuthor() {
        return bookAuthor;
    }

    public void setBookAuthor(String bookAuthor) {
        this.bookAuthor = bookAuthor;
    }

    public String getBooktype() {
        return booktype;
    }

    public void setBooktype(String booktype) {
        this.booktype = booktype;
    }

    public int getNoOfBooks() {
        return noOfBooks;
    }

    public void setNoOfBooks(int noOfBooks) {
        this.noOfBooks = noOfBooks;
    }

    public String getfName() {
        return fName;
    }

    public void setfName(String fName) {
        this.fName = fName;
    }

    public String getlName() {
        return lName;
    }

    public void setlName(String lName) {
        this.lName = lName;
    }

    public String getBuyerName() {
        return buyerName;
    }

    public void setBuyerName(String buyerName) {
        this.buyerName = buyerName;
    }

    public String getTrackCartUser() {
        return trackCartUser;
    }

    public void setTrackCartUser(String trackCartUser) {
        this.trackCartUser = trackCartUser;
    }
    
}
